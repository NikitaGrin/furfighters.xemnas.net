        <h1>Visual Comparisons - Anatat Tatanatat Levels</h1>
This page lists differences between the levels and Fluffmatches found within Anatat Tatanatat.<br /><br />

<h2>Anatat Tatanatat</h2><center>
<img src="img/com/anatat-dc1.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"> <img src="img/com/anatat-pc1.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"><br />
The PC and all subsequent releases removed a fairly significant stretch of road leading to the broken tree. Needless to say, this also affected the tree's appearance slightly. <br /><br />
<img src="img/com/anatat-dc.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"> <img src="img/com/anatat-pc.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"><br />
The PC and all subsequent releases spruced up the entrance to the Jungle of Despair.  <br /><br />
<img src="img/com/anatat-dc2.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"> <img src="img/com/anatat-pc2.jpg" alt="Anatat Tatanatat Comparison" title="Anatat Tatanatat Comparison" width="300"><br />
The PC and all subsequent releases added a tree near the top entrance to the waterfall. This is likely because anyone gliding the other way can easily slip out of the level boundaries, and it just looks an eyesore in general when you look that way.  <br /><br /></center>

<h2>Raiders of the Lost Fluff</h2><center>
<img src="img/com/raiderpc1.jpg" alt="Raiders of the Lost Fluff Comparison" title="Raiders of the Lost Fluff Comparison" width="300"> <img src="img/com/raiderps1.jpg" alt="Raiders of the Lost Fluff Comparison" title="Raiders of the Lost Fluff Comparison" width="300"><br />
This area was removed. Very literally.<br /><br />

<img src="img/com/raiderpc2.jpg" alt="Raiders of the Lost Fluff Comparison" title="Raiders of the Lost Fluff Comparison" width="300"> <img src="img/com/raiderps2.jpg" alt="Raiders of the Lost Fluff Comparison" title="Raiders of the Lost Fluff Comparison" width="300"><br />
The foliage has been covered up by a wall, and there are now additional goodies (and extra stairs) here in Viggo's Revenge<br /><br />
</center>
