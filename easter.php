<h2>Bizarre's Bizarre Easter Egg</h2>
In Land of the Happy Moles, the computers are actually displaying real screenshots from Bizarre Creations' former office. Notably, one of the screens actually shows the level being designed.<br /><br />
<center><img src="img/lotm.png" alt="Land of the Happy Moles" title="Land of the Happy Moles"></center><br /><br />
Also in Land of the Happy Moles, there is an arcade machine. This is the based off the same one that could be found in their office, as seen in the image from <a href=http://venturebeat.com/2010/03/04/bizarre-creations-studio-tour-and-blur-press-event/>Venturebeat</a>.<br /><br />
<center><img src="img/arcade.png" alt="Land of the Happy Moles" title="Land of the Happy Moles"></center><br />
<br />
Finally, in the PC adaption there is a cabinet containing a Dreamcast and a couple of games, including Fur Fighters.<br /><br />
