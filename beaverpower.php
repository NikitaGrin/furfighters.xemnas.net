</center>
<img src="guide/bp-0.png" alt="Beaver Power" title="Beaver Power" align="right"  style="margin:0px 6px">Here we are at Beaver Power, home to some of the most beautiful forests in the world... Soon to be buried under concrete. Almost as soon as you try to go anywhere, General Bristol will arrive to let you know your objectives this time. Helpful, but it might get a bit tiring since he's going to go through the exact same talk everytime you enter and exit Beaver Power. Anyway. Let's kick off by grabbing the Gold Tokens, taking care of any Bears you come across. Turn right near the start to find a trail of 3 Gold Tokens leading to Chang's Telepoint. Now you'll want to follow the path forward, and turn left when you see an opening. Complete Sergeant Sternhauser's challenge, and he'll destroy the little well, so you can grab the 4 Golden Tokens hidden under it. Now follow the main path again, and switch to <b>Rico</b> when you see his Telepoint just past the entrance to Beaver Dam. Press the buttons for the elevator, and at the top here, take care of the lone Brown Bear and face the S.S.Beaver ship. You'll see a little break in the fence, where two Gold Tokens are suspended in the air. Grab them (you may need to get back up here again if you miss one) and, when you land in the water, swim underneath the ship to find the final Token. To get out, rise to the top and on your right there's a step up to land. And more Bears, so be careful.<br /><br />

