        <h1>Visual Comparisons - New Quack City</h1>
This page lists differences between the levels and Fluffmatches found within New Quack City.<br /><br />
<h2>New Quack City</h2>
<center>
<img src="img/com/nqc-dc1.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc1.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
<img src="img/com/nqc-ps1.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
The starting point changes with each version. Dreamcast (far left) places the Chang Telepoint behind you. The PC (center) removes the Telepoint, and adds in a large rock just opposite. Finally, PS2 (far right) removes the pointless rock, narrows the space a bit and flips the signage, since in this version you go the opposite way at first.<br /><br />
<img src="img/com/nqc-dc2.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc2.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
The Dreamcast version is pretty bland here. The PC adds in a rock for General Bristol to appear on, and Tweek's Telepoint has been moved from inside the lake to just outside. Oh, and a shiny new signpost has been added.<br /><br />
<img src="img/com/nqc-dc6.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc6.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
As stated above, Tweek's Telepoint has been moved in the PC version. The Dreamcast version has the Telepoint and an additional signpost here, as well as a good amount of empty space. The PC version also has a small slope leading down there.
<br /><br />
<img src="img/com/nqc-dc3.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc3.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
The PC corrected the Dreamcast's huge signpost. Also, Chang's Telepoint was moved over here as opposed to the beginning of the level in the Dreamcast version.<br /><br />
<img src="img/com/nqc-dc4.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc4.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
Some extra pathway in the Dreamcast version was removed in the PC version, likely since it served no real purpose.
<br /><br />
<img src="img/com/nqc-dc5.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-pc5.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
There's a gray rock here in the Dreamcast version. This is removed and a much more helpful signpost is added nearby instead.
<br /><br />
<img src="img/com/nqc-pc7.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-os1.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
One of the extremely rare Viggo on Glass (iOS) comparisons here. The World Quack Centre was renamed 'Empire Quack Building' (for obvious reasons). An entirely new font was used to redo the sign posts.
<br /><br />
<img src="img/com/nqc-pc8.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300">
<img src="img/com/nqc-os2.jpg" alt="New Quack City Comparison" title="New Quack City Comparison" width="300"><br />
Oh, and on that note - Fur Fighter Village is now 'Fur Fighters Village'.
<br /><br />
</center>

<h2>World Quack Centre</h2>
<center>
<br /><br />
<img src="img/com/wqc-pc6.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-ios1.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
The name was changed in the iOS version to Empire Quack Building for obvious reasons. As such, the text was changed here to reflect that, and the image of two towers reduced to one.
<br /><br />
<img src="img/com/wqc-dc1.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-pc1.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
The Dreamcast version looks a bit bare here, so some taxis were added in future installments.
<br /><br />
<img src="img/com/wqc-dc2.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-pc2.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
Although I don't want to get into the habit of pointing out enemy location differences, this one deserves it. Two Brown Bears were added in the enclosed areas which are empty in the Dreamcast version.
<br /><br />
<img src="img/com/wqc-dc3.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-pc3.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
Sergeant Sternhauser game change! Only the Dreamcast has the colour coded set of mats.
<br /><br />
<img src="img/com/wqc-dc4.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-pc4.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
There's a 'regular' screen here in the PC version. All future installments feature this Bizarre Creations screen. Shooting it unlocks the fluffmatch, Land of the Happy Moles (though not in Viggo on Glass, since there aren't any Fluffmatches there).
<br /><br />
<img src="img/com/wqc-dc5.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300">
<img src="img/com/wqc-pc5.jpg" alt="World Quack Centre Comparison" title="World Quack Centre Comparison" width="300"><br />
Rico's Telepoint is on this diving board in the Dreamcast game, but a slide was added in future installments, and Rico's Telepoint was moved over. General Bristol now resides at the end of the diving board instead.
<br /><br />
</center>

<h2>Lower East Quack</h2>
<center>
<img src="img/com/leq-dc1.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc1.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
The gateway to Upper East Quack is a wooden door in the Dreamcast version, but has changed to a blue turnstile in all other versions. The position of the gate has also been changed.
<br /><br />
<img src="img/com/leq-pc8.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-ps2.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
Near the subway exit is Chang's Flea Level entrance in the PS2 version. There's nothing more than a wall in the other versions, though.
<br /><br />
<img src="img/com/leq-dc2.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc2.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
The walls are bare in the Dreamcast version. A Pet Yums Junior sign has been added to the wall, along with a mirror and fire extinguisher in the corner.
<br /><br />
<img src="img/com/leq-dc5.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc5.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
An advertising poster for Ducks and one for General Viggo's mayor election have been added.
<br /><br />
<img src="img/com/leq-dc4.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc4.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
An advert for Apocalypse Cow and The Thin Red Lion have been added, along with another mirror and fire extinguisher.
<br /><br />
<img src="img/com/leq-dc3.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc3.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
Another Pet Yums Junior advert and another mirror have been added.
<br /><br />
<img src="img/com/leq-dc6.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-pc6.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
The Dreamcast version doesn't have a lot going for it. The PC version changed the switch to a valve, added some pipes and also added an arrow. This could be because the water has also been changed from clear to... Well. It's a sewer. Use your imagination (or not).
<br /><br />
<img src="img/com/leq-pc7.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300">
<img src="img/com/leq-ps1.jpg" alt="Lower East Quack Comparison" title="Lower East Quack Comparison" width="300"><br />
One of the buildings has been changed in the PS2 version. Gwynth didn't like the change, so she set it on fire.
<br /><br />
</center>

<h2>Quackenheim Museum</h2>
<center>
<img src="img/com/qm-dc1.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc1.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
In the Dreamcast version, this cabinet holds a Rocket Launcher. In future games, this is where you rescue Leonard.<br /><br />
<img src="img/com/qm-dc2.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc2.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
In the Dreamcast version, you can rescue Petey on this glass cabinet. The Rocket Launcher has been moved here in future games instead.<br /><br />
<img src="img/com/qm-dc3.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc3.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
Dreamcast version has a Bomb Launcher at the top of the pillar, but in other games, Petey was moved here.<br /><br />
<img src="img/com/qm-dc4.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc4.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><img src="img/com/qm-ps1.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
The Fire Door is missing part of the texture and looks rather odd in the Dreamcast version. PC altered the door so that it fit. And PS2 added a new door and altered the original door so that you can see Pweek in just behind it.<br /><br />
<img src="img/com/qm-dc5.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc5.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
Part of the door from the main room (the one used when the Chameleon Bot appears to prevent you from leaving) shows in the Dreamcast version, but all future games fixed that. There's also a wall texture change, while we're here.<br /><br />
<img src="img/com/qm-dc6.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-pc6.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
This is where you rescue Leonard in the Dreamcast version. There's nothing on the platform in all other versions.<br /><br />
<img src="img/com/qm-dc7.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300">
<img src="img/com/qm-ps2.jpg" alt="Quackenheim Museum Comparison" title="Quackenheim Museum Comparison" width="300"><br />
The original games feature a Security Guard in charge of the boiler room. The PS2 version adds a completely unique model - The Boiler Room Guard. He's also pushed closer to the wall than the Security Guard.<br /><br />
</center>


<h2>Gangsta Fluffin'</h2><center>
<img src="img/com/gfpc-1.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-1.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
A decently sized area was removed from this level, thus the entrance on the right has been closed up. The Pet Yum cans are in the original too, but they don't show up well in this pic. Also, note the lack of snow in Viggo's Revenge.<br /><br />
<img src="img/com/gfpc-5.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-5.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
This hole on the next floor leading into the former room below has been sealed up.<br /><br />
<img src="img/com/gfpc-2.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-2.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
In the original version there's an area which you can enter here, showing off the building inside. However, this has been completely sealed up in Viggo's Revenge and the hallways removed. All that remains from this area is the wall texture, which can only be seen if the free camera is used to view inside.<br /><br />
<img src="img/com/gfpc-3.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-3.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
Once again, the area has been sealed up with a bright yellow window. The lift is still there, but it's been moved up a level.<br /><br />
<img src="img/com/gfpc-4.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-4.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
A plank was added. In the original version, the crane will move the building equipment over to you, and you can ride it to the other side. In Viggo's Revenge, the crane no longer moves over anywhere near enough for you to jump over. You <i>can</i> still glide over here with Tweek (if you're skilled enough, since it takes fairly precise jumping/gliding). Also, Tweek's Telepoint is just to the left here in Viggo's Revenge.<br /><br />
<img src="img/com/gfpc-6.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-6.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
If you do get over there in Viggo's Revenge, you'll find a mini barren wasteland. The Pet Yums Plus and Flame Gun have been removed entirely, and Tweek's Telepoint is now in a different place.<br /><br />
<img src="img/com/gfpc-7.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"> <img src="img/com/gfps2-7.jpg" alt="Gangsta Fluffin' Comparison" title="Gangsta Fluffin' Comparison" width="300"><br />
Nothing major, but the snow has been removed from most planks.<br /><br />
</center>

