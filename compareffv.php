        <h1>Visual Comparisons - Fur Fighter Village</h1>
Since Fur Fighter Village has the most revisions between all versions, this is split up by locations. This page also covers the Undermill and Land of the Happy Moles (coming soon).<br /><br />

<h2>Roofus' Home</h2><center>
<img src="img/com/ffv/ff-pc13.jpg" alt="Roofus' Home Comparison" title="Roofus' Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr13.jpg" alt="Roofus' Home Comparison" title="Roofus' Home Comparison" width="300"> <br /> Army helmet added on top of wardrobe (which has been slimmed down), bookshelf added, window changed with foliage sticking out and army medals on display.<br /><br />
<img src="img/com/ffv/ffv-pc13.jpg" alt="Roofus' Home Comparison" title="Roofus' Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr15.jpg" alt="Roofus' Home Comparison" title="Roofus' Home Comparison" width="300"> <br />Scotland's flag was added behind the bar. Bit of trivia - <a href="beta2.php">An early image</a> shows the flag present in the Dreamcast version too, but it's removed in the final game.<br /><br />
</center>
<h2>Bungalow's Home</h2><center>
<img src="img/com/ffv/ffv-dc.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"> <img src="img/com/ffv/ffv-pc.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"><br />
Bomber Bear's background grass graphics have been altered. <br /><br />
<img src="img/com/ffv/ffv-pc10.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr11.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"><br />
Esmerelda's pots and pans have been added, along with a new picture.<br /><br />
<img src="img/com/ffv/ffv-pc11.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr10.jpg" alt="Bungalow's Home Comparison" title="Bungalow's Home Comparison" width="300"><br />
A refridgerator and sink have been added.<br /><br />
</center>
<h2>Juliette's Home</h2><center>
<img src="img/com/ffv/ffv-pc4.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr4.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"><br />
Juliette's closet got a huge makeover. After entering and turning a few corners, you end up inside a room which stretches up as far as the eye can see. The real difference is that a working mirror has been added here.<br /><br />
<img src="img/com/ffv/ffv-pc5.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr5.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"><br />
Complete overhaul, design wise. Even the slope has been converted to actual stairs.<br /><br />
<img src="img/com/ffv/ffv-pc6.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr6.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"><br />More new trendy designs from Juliette and Claude. For whatever reason, the pictures have been swapped.<br /><br />
<img src="img/com/ffv/ffv-pc7.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr7.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"><br />
Sculpture pushed back, new closet in place.<br /><br />
<img src="img/com/ffv/gamedc-1.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"> <img src="img/com/ffv/gamepc-1.jpg" alt="Juliette's Home Comparison" title="Juliette's Home Comparison" width="300"><br />
Game graphics have changed - You're now aiming for a bubble instead of a house. Also - Juliette's family portrait is noticeably unfinished in the Dreamcast version.<br /><br />
</center>
<h2>Chang's Home</h2><center>
<img src="img/com/ffv/gamedc-2.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"> <img src="img/com/ffv/game-pc.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"><br />
Game graphics were updated.<br /><br />
<img src="img/com/ffv/ffv-pc8.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr8.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"><br />
Tiger poster transferred into a rug, bookshelf with roots growing now in its place. A computer has also been added.  Telepoint slightly moved over.<br /><br />
<img src="img/com/ffv/ffv-pc9.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr9.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"><br />Dragon poster removed, new bookshelf in place.<br /><br />
<img src="img/com/ffv/ffv-dc3.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"> <img src="img/com/ffv/ffv-pc3.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"><br /> <img src="img/com/ffv/ffv-vr3.jpg" alt="Chang's Home Comparison" title="Chang's Home Comparison" width="300"><br />In PC, Chang's family portraits were added. In Viggo's Revenge, they are replaced by even more bookshelves.<br /><br />
</center>
<h2>Rico's Home</h2><center>
<img src="img/com/ffv/ffv-pc12.jpg" alt="Rico's Home Comparison" title="Rico's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr12.jpg" alt="Rico's Home Comparison" title="Rico's Home Comparison" width="300"> <br />A cloth and pot plant have been added to the bench.<br /><br />
</center>
<h2>Tweek's Home</h2><center>
<img src="img/com/ffv/ffv-pc14.jpg" alt="Tweek's Home Comparison" title="Tweek's Home Comparison" width="300"> <img src="img/com/ffv/ffv-vr14.jpg" alt="Tweek's Home Comparison" title="Tweek's Home Comparison" width="300"> <br />A train and working fire were added.<br /><br />
</center>
<h2>General</h2><center>
<img src="img/com/ffv/opc-1.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <img src="img/com/ffv/ovr-1.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <br />Even before Viggo A Gogo is opened, you can still see the ship in Viggo's Revenge.<br /><br />
<img src="img/com/ffv/opc-2.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <img src="img/com/ffv/ovr-2.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <br />A table was added for the Safety Certificate.<br /><br />
<img src="img/com/ffv/opc-3.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <img src="img/com/ffv/ovr-3.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <br />Two stumps were added for the Bullwhip and Fedora. The Beetle now 'worships' the ground.<br /><br />
<img src="img/com/ffv/opc-4.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <img src="img/com/ffv/ovr-4.jpg" alt="Fur Fighter Village Comparison" title="Fur Fighter Village Comparison" width="300"> <br />Security Guard Duck is now an Assistant Duck, plus a table was added for the Degree in Astrophysics.<br /><br />
</center>


<h2>Undermill</h2><center>
<img src="img/com/underdc.jpg" alt="The Undermill Comparison" title="The Undermill Comparison" width="300"> <img src="img/com/underpc.jpg" alt="The Undermill" title="The Undermill" width="300"><br />
The PC and all subsequent releases added a podium for Angela. <br /><br />
<img src="img/com/underdc1.jpg" alt="The Undermill Comparison" title="The Undermill Comparison" width="300"> <img src="img/com/underpc1.jpg" alt="The Undermill" title="The Undermill" width="300"><br />
Another podium was added, this time for the Gold Tokens. <br /><br />
<img src="img/com/underdc2.jpg" alt="The Undermill Comparison" title="The Undermill Comparison" width="300"> <img src="img/com/underpc2.jpg" alt="The Undermill" title="The Undermill" width="300"><br />
And yet another podium was added for Sergeant Sternhauser and General Bristol to stand on. Also, a can of Super Pet Yums has been added, likely to avoid players fluffing themselves during the cutscene.<br /><br />
</center>

