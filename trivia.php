        <h1>Trivia</h1>
So we've got our <a href="easter.php">Easter Eggs</a>, <a href="mod.php">Unused models</a>, and  <a href="beta.php">Unused Elements</a> pages all covering a large portion of these. But this particular trivia don't really fit directly in any of these places!<br /><br />

<h2>Tokens: 2K+ Edition</h2>
In all but Fur Fighters: Viggo's Revenge, it is possible to get over 2000 Gold Tokens by collecting the Hubworld tokens as well.<br /><br /><center>
<img src="img/2008.PNG" alt="Gold Tokens" title="Gold Tokens" border="0" width="300"></center>
<br />
<h2>Like Father, Like Son</h2>
Or like mother, like daughter, like siblings, etc. The voices for the Fur Fighter babies are actually their parent's (or brother's) voice sped up by around 30%. <a href="voices/chang-voice.mp3">Take Chang, for example</a>.<br /><br />

<h2>The Button's Gone, That's Why</h2>
Although you can't actually enter them, the closed off elevators all share the same model inside, albeit without the button. This was likely done to save time when they were being made.<br /><br /><center>
<img src="img/elev-1.jpg" alt="Elevator" title="Elevator" border="0">
<img src="img/elev-2.jpg" alt="Elevator" title="Elevator" border="0">
</center>
<br /><br />

<h2>Viggo's Hidden Phrases</h2>
Outside the level boundaries, General Viggo can be found with a generic backdrop in a few levels. They're only supposed to be used for cutscenes, but if you enter it and attack the general then not only does he use his walk forward movement, he also says these words with a fantastic imitation...<br /><br /><center>
<img src="img/beavercat.jpg" alt="Beaver Cat" title="Beaver Cat" border="0"></center>
<br /><br />

<h2>Tweek's Missing Sibling</h2>
Gwynth's family portrait shows 15 babies, excluding Tweek. But in the game, there are only 14 baby dragons to rescue. What happened to Tweek's final sibling, I wonder?<br /><br /><center>
<img src="img/gwynthfam.jpg" alt="Gwynth's Family" title="Gwynth's Family" border="0"></center>
<br /><br />

<h2>Manly Juliette</h2>
The Manager at O'Quackers either has a weird sense of humor, or just didn't take a proper look at dear Juliette.<br /><br /><center><img src="img/he.jpg" alt="Didn't HE do well!" title="Didn't HE do well!" border="0" width="350"></center>
<br /><br />

<h2>Unobtainable obtainable Pet Yums</h2>
In Temple of Gloom, there is a can of Pet Yums underneath the moulding machines. However, once you get down there, there is no way out. You need to either wait until the heat depletes your HP or, if you're using the Invulnerability cheat, exit the level.<br /><br /><center>
<img src="img/petyums.jpg" alt="Unobtainable Pet Yums" title="Unobtainable Pet Yums" border="0" width="300"></center>
<br /><br />

<h2>Mysterious Elevator Button</h2>
Seen only in the original Dreamcast game, there is a strange button behind the Secret Elevator in the World Quack Centre, just underneath the 10T weight. Pressing or shooting it does nothing, and it has been removed in all future editions.<br /><br /><center>
<img src="img/secretbutton.jpg" alt="Secret Button" title="Secret Button" border="0" width="300"> <img src="img/secretbutton2.jpg" alt="Secret Button" title="Secret Button" border="0" width="300"></center>
<br /><br />

<h2>Dino Deco Level</h2>
On some NTSC copies of the game, the back cover mentions a level called 'Dino Deco'. Of course, there is no such level in the final game. It is in fact Dinotopolis' early name, as seen in the <a href="beta.php">Unused Elements</a> page.<br /><br /><center>
<img src="img/dinodeco.jpg" alt="Dino Deco" title="Dino Deco" border="0"></center><br /><br />
<h2>Shrimp-Lobster</h2>
In the Official Strategy Guide, as well as the game's coding, these infamous enemies are known as Shrimps. But in Dinotopolis, a building block refers to them as a Lobster. So what are you, my dear foe?<br /><br /><center>
<img src="img/lobster.png" alt="Lobster Block" title="Lobster Block" border="0"> <img src="img/shrimp.jpg" alt="Shrimp Strategy" title="Shrimp Strategy" border="0"></center><br /><br />
<h2>Missing Sounds</h2>
The Fluff Resurrection Fluffmatch  is a parody of the ever popular film Aliens. Or at least, it is in the PC version. As will be documented shortly in the <a href="compare.php">comparisons</a> section, the level underwent a huge remodelling and wound up being barely recognisable in Viggo's Revenge. This made the level a lot less interesting. However, on top of that, some eerie background sounds were cut from the level, making them entirely absent from Viggo's Revenge as a whole. <a href="voices/fluffres.mp3">Click here</a> to listen.<br /><br />


