        <h1>Test & Unused Levels</h1>
There are several levels that go unused in Fur Fighters. Some of these were used for testing the game in some way. But a couple of these are actual levels that have been removed from the game for one reason or another. We'll start off with the unused levels first, then delve into the test levels of the games.
<br /><br /><b>Quick Links</b><br />
<a href="#nqs">New Quack Subway</a> • <a href="#unclegravy">Uncle Gravy</a> • <a href="#pooltest">Pool Test</a> • <a href="#chartest">Char Test</a><br /><br />
<a id="#nqs"></a><h2>New Quack Subway</h2> 
This level was known for quite some time, but only discovered in playable format in June 2015 (by yours truly). It exists in both the PC and PS2 versions (and probably the DC version). The PC version is strangely 'hidden' in the models folder, under new_ykl1.lpc. Attempts to load the PC version result in an error, since the PC file is a whopping 15mb+ in size - That's triple the amount of any other level! With that in mind, this is either the finished level, or a garbage-filled level. Viggo's Revenge has a much smaller file, however, and is playable once certain methods have been applied.<br /><br />
This level was fairly far along in development, with a good amount of the level model looking pretty good. Default textures are rampant here, with barely any finalised textures present. Much of the level has no texture at all, and other parts use textures from other levels. The level is RAMPANT with Brown Bears, the only enemies in this place. The Beavers are also in this level, which, coupled with the level title in the PC version means that this could very well have been the original first level of New Quack City, before even the birds were created. There is no real text file for this level, and instead it uses a clone of the World Quack Center's file. With that in mind, let's get started.<br /><br /><br />

<img src="img/nq4-1.jpg" alt="New Quack Subway" title="New Quack Subway"> <img src="img/nq4-2.jpg" alt="New Quack Subway" title="New Quack Subway"> 
<br />We start out here, which... Isn't much of a starting point. There's a Brown Bear to the left who will start firing as soon as you enter the level. Of course, the more interesting sight is the one directly before us. A Hippie Beaver and his open guitar case. When you approach him, he changes his movement, but since this he's a placeholder, he just runs forward instead. And of course, the Entry Permit. It still counts as a major key item, but it's used as a regular item in this level. You can't do anything else here. Behind the starting point is a set of stairs, leading to another area.<br /><br />

<img src="http://i.imgur.com/8hHbAlA.gif" alt="New Quack Subway" title="New Quack Subway"> <img src="img/nq4-4.jpg" alt="New Quack Subway" title="New Quack Subway"><br />Now these are <b>very</b> interesting! As you can see in the image, collecting one of these actually counts as <i>25 Gold Tokens</i>. In the PC version, there are also files for 5 & 10 Gold Tokens as well. These actually regenerate after a while, too. <br /><br />

<img src="img/nq4-5.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-6.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />To the left is a set of stairs that split apart and lead straight into walls. Needless to say, the texture suggests that doorways were to be added here. <br />To the right is this structure, with a globe on top.
<br /><br />

<img src="img/nq4-7.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-8.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Before turning left, straight ahead there's a couple of entrances which both lead to a rather barren room. Straight ahead are two windows, with a Beaver in one of them (complete with his lunch box and quarter-eaten sandwich). You can talk to him, but like the other characters here, he doesn't have any text assigned. If you talk to him whilst in possession of the Entry Permit, he'll take it from you and place another Entry Permit on the floor. Thanks, pal. I guess.
<br /><br />

<img src="img/nq4-9.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-10.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Further left of the 25 Gold Token items we saw just a little back is this giant default blue textured structure. And yes, that's exactly what you think it is. Go up to flights of stairs to find a larger set of stairs and these two red default textured trains. You can go behind them, but there's nothing there.
<br /><br />

<img src="img/nq4-11.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-12.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Going up, it leads to yet another set of stairs which leads back down to an alternate area. There's a Tweek Telepoint here, just behind the blue bars
<br /><br />

<img src="img/nq4-13.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-14.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />On the right is another red train, on the left is a pathway leading to nothing (same as what's behind that red train, actually). Since you can't jump back up here, a ladder has been added on either side.
<br /><br />

<img src="img/nq4-15.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-16.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Back to the blue structure outside, there's a Bungalow Telepoint on the left, atop the red train. You can walk along the beams to rescue... Angel. Well, sure, this level <i>is</i> still using World Quack Center's text file, after all.
<br /><br />

From here, we need to go back to the beginning of the level.<br /><br />
<img src="img/nq4-17.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-18.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Back to the start. Taking a right turn and going down these working up-and-down escalators (with static floor images) takes us into what looks like either an early information desk or ticket vendor using a default texture. There's a lotta bears here, too. At the back is an indent in the wall with no texture, suggesting that a wall was to be implemented here.
<br /><br />

<img src="img/nq4-19.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-20.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Turning left down the slope in that room and taking an immediate right at the bottom leads to a chain link fence (and a whole load more bears).
<br /><br />

<img src="img/nq4-21.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-22.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Carry on here, and you'll come out at a working subway. Two trains will semi-frequently rush through at a good speed, causing an insta-fluff if you're in the way. Walking down the stairs and turning right will lead to a small cove where another bear is waiting, as well as a crocodile! Makes a change.
<br /><br />

<img src="img/nq4-23.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-24.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Touching the (metal part of the) tracks will cause damage, but only while you're actually moving on there. Like before, there's a ladder on each side (at both ends here) to climb up. The only option on this side is to climb few more flights of stairs, where another crocodile and brown bear await mid-way.
<br /><br />

<img src="img/nq4-25.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-26.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />There's a textureless turnstile gate here, which will only open from this side. It will close once you've passed it. This doesn't matter at this stage, however, since you can leap over it from either side anyway. After that, another near-empty room with bears in it, and more stairs.
<br /><br />

<img src="img/nq4-27.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-28.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Following those up will show another room on the left, where a few different things are (and more bears too). This water barrel, for instance, is another unique item, quite different in appearance to the one seen in Temple of Gloom. There's a ladder leading up to the top (which neither Tweek nor Bungalow can actually reach thanks to the platform it's standing on) and inside, it's filled with functioning water. Another ladder is inside to help get the Fur Fighter out of there.
<br /><br />

<img src="img/nq4-29.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-30.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Next to that, are these two objects. A default textured box, a default textured platform and an object which looks a bit like the one seen in H.M.S. Viggolina, with no texture again. It's completely static and despite appearing to have a button, nothing works when interacting with it.
<br /><br />

<img src="img/nq4-31.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-32.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />There are two other ways to go, but since you come back the other way soon, it's better to go forward from the last door and take the (static but working) escalator up, admiring the beautiful temporary playceholder images from Furry Forest whilst you're at it. At the top, there's another placeholder where you would have just come from, and two pathways. The one furthest away (on Bungalow's right in the image) leads back to the 'beginning' of the level.
<br /><br />

<img src="img/nq4-33.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-34.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />Taking the other escalator up will lead to another barren area and turning left leads to another bear in another fairly barren space. However, there's another beaver behind glass here.
<br /><br />

<img src="img/nq4-35.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-36.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />He'll try to talk, and show a clip of another train. A timer shows up, and - you've guessed it - You have to make a beeline back to that other room that we skipped.
<br /><br />


<img src="img/nq4-37.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-38.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />This one. And by the way, there's nothing in those chain gates - Just a blank room. Down here is the train the Beaver showed you earlier, as well as the Elder Beaver (who attempts to talk to you when you approach, but still no assigned text).
<br /><br />

<img src="img/nq4-39.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-40.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />The timer isn't quite as generous as it could be, and if you're not careful, you could slip down the side and the train will leave without you. Not as bad as it seems though, since there are four small areas down here that you can get into. There are ladders here too, for those not playing as Bungalow. At this stage in the game, you can just follow them across if you missed the train (which, by the way, does not initiate a cutscene or anything as it goes along). The 'move and get hurt' rule still applies here, but just stay off the metallic parts to avoid it - The wood is safe.
<br /><br />

<img src="img/nq4-41.jpg" alt="New Quack Subway" title="New Quack Subway">
<img src="img/nq4-42.jpg" alt="New Quack Subway" title="New Quack Subway">
<br />At the end of the area (careful if you're on the train - You'll want to leave immediately or it will go all the way back shortly after it hits the wooden barrier), there's a small area for you to stand on and in front of you are two final train carriages, looking suspiciously like the ones you boarded in Fur Fighter Village. You can walk straight underneath them too since there are no railings or wheels down here, but be warned - If you get down here as Tweek, there is no way back up. There's a platform behind the trains which shows that they are hollow and have no back texture assigned.
<br /><br /><br />

And that's it for New Quack Subway, aka New Quack City Level 4. The level seems as though it was pretty far into production before it was ultimately scrapped. Some interesting notes about this level:<br />

<li>This is the only level which contains the 25 Gold Token nugget. No known pre-release shots show this item.</li>
<li>This level also confirms that 5 and 10 Gold Token pickups were also at one stage planned. The files can all be found in the PC version (along with the 25), but cannot be accessed at this time.</li>

<a id="unclegravy"></a><h2>Uncle Gravy</h2>
By far the strangest of the lot. This level can be accessed using a Gameshark code to switch on the level select. It can be accessed via that level select, or by selecting 'Return to Hub' whilst in Pool Test. Whilst the level title can be found in the files for both Dreamcast and PC, the level only exists on the PlayStation 2 release, Viggo's Revenge.
<br /><br /><center>
<a href="img/unclegravy1.png"><img src="img/unclegravy1.png" alt="Uncle Gravy" title="Uncle Gravy" border="0" width="300"></a> <a href="img/unclegravy2.png"><img src="img/unclegravy2.png" alt="Uncle Gravy" title="Uncle Gravy"  width="300" border="0"></a></center>
<br />
Really, these two images show everything. Honest. Click on the images to make them bigger (or don't, you're not missing much). The animation for these four models (found in the cutscene for completing Saving Winnie & Mai) repeats over and over again, floating slightly above where you stand and devoid of any shading (unlike your Fur Fighter). There is no collision detection present, so you can walk straight through them as you please. The platform you're on is a pure black plane, and uses the same sound for walking on regular ground. The skybox is the same one used in most Anatat Tatanatat levels. The music? There is none. The only sound effects programmed besides the sound of your Fur Fighter running are the ones you create (gun fire, jumping,etc.). There is literally nothing else here. Overall, a very lonely level that was no doubt used for various testing at different stages of the game's development.
<br />
<a id="pooltest"></a><h2>Pool Test (Dreamcast)</h2> 
This is a simple level, although the name is up for interpretation. Whether it's an old name, random name or based on the pool of enemies can be left for you to decide.
<br /><br /><center>
<a href="img/pooltest1.png"><img src="img/pooltest1.png" alt="Pool Test" title="Pool Test" border="0" width="300"></a> <a href="img/pooltest2.png"><img src="img/pooltest2.png" alt="Pool Test" title="Pool Test"  width="300" border="0"></a></center>
<br />
This is basically a testing ground for the enemies. It has one of each regular enemy found in the game, using Beaver Power outfits (or, for those who appear later such as the Fox, whichever level they appear in first). You're attacked immediately, making this level almost impossible to complete without using the Invulnerability cheat.
<br /><br /><center>
<a href="img/pooltest3.png"><img src="img/pooltest3.png" alt="Pool Test" title="Pool Test" border="0" width="300"></a> <a href="img/pooltest4.png"><img src="img/pooltest4.png" alt="Pool Test" title="Pool Test"  width="300" border="0"></a></center>
<br />
The small area is an enclosed box, using a default texture. Zooming out reveals the skybox to be New Quack City's, and the song that plays is General Viggo's theme.

<br /><br /><center>
<a href="img/pooltest5.png"><img src="img/pooltest5.png" alt="Pool Test" title="Pool Test" border="0" width="300"></a></center>
<br />
Finally, the level has some time attacks coded in, plus an image of Cape Canardo as the image for the level. Returning to hub crashes the game, since there is no testing hub still present on the Dreamcast discs.
<br />
<a id="chartest"></a><h2>Char Test</h2>
The title is appropriate for this Dreamcast exclusive level, since there's a Telepoint for everyone here. But more importantly, there's one of nearly item found in the game, including the <a href="mod.php">unused ones</a>.
<br /><br /><center>
<a href="img/chartest1.png"><img src="img/chartest1.png" alt="Char Test" title="Char Test" border="0" width="300"></a> <a href="img/chartest2.png"><img src="img/chartest2.png" alt="Char Test" title="Char Test" border="0" width="300"></a><br /></center>
You start off with Rico in this large open space which uses a default texture with an Anatat Tatanatat skybox. Note that not all items can be seen at the same time. Also, like Uncle Gravy, there's no music here at all, just sound effects.<br /><br /><center>
<a href="img/chartest3.png"><img src="img/chartest3.png" alt="Char Test" title="Char Test" border="0" width="300"></a> <a href="img/chartest4.png"><img src="img/chartest4.png" alt="Char Test" title="Char Test" border="0" width="300"></a><br /></center>
There's one of each type of weapon (first tier only, save for the Pistol which also has a Submachine Gun), as well as each Pet Yums can and one of each Telepoint.<center>
<br /><br /><a href="img/chartest5.png"><img src="img/chartest5.png" alt="Char Test" title="Char Test" border="0" width="300"></a> <a href="img/chartest6.png"><img src="img/chartest6.png" alt="Char Test" title="Char Test" border="0" width="300"></a><br /></center>
Every level item and key item is here. Also, the three powerups are present for testing too.
<center>
<br /><br /><a href="img/chartest7.png"><img src="img/chartest7.png" alt="Char Test" title="Char Test" border="0" width="300"></a> <a href="img/chartest8.png"><img src="img/chartest8.png" alt="Char Test" title="Char Test" border="0" width="300"></a><br /></center>
Even the unused items, such as the Widget and Builder's Hat are here. Although no items show up after collection, the key items do. And strangely, they all fit on the same bar, even though through regular gameplay the maximum you ever have is 2.
<center>
<br /><br /><a href="img/chartest9.png"><img src="img/chartest9.png" alt="Char Test" title="Char Test" border="0" width="300"></a> <a href="img/chartest10.png"><img src="img/chartest10.png" alt="Char Test" title="Char Test" border="0" width="300"></a><br /></center>
Finally, there's this ditch which, when a Fur Fighter falls in, they can easily climb back out from any side. Juliette is also able to move sideways, something the others are unable to do.


