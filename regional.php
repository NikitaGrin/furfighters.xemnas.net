<h2>Name Swap</h2>
In the US release of Fur Fighters, Juan Luis and Wu have their names swapped by accident. The European release does not have this problem.

<h2>Language Select</h2>
Like with most games, Fur Fighters offers a language select to the European game only. The flags are still present in the US coding, however.

<h2>Cheat Difference</h2>
The US version of Fur Fighters: Viggo's Revenge allows the Blood Cheat to be unlocked, but not actually used. Other copies of the game allow the cheat to be used  without a problem.

