<h1>Regional Differences</h1>
Although not many, there are a few differences between the European and US versions of the game.

<h2>Text Differences</h2>
There are a lot of overall changes, in the PC version at least. The vast majority of them are fixing spellings so that they correspond with the country they're aimed at. For example - Colour to Color. Many of them are also grammar fixes, with many additional commas added in to the US version. In some instances, such as when General Bristol mentions the  World Quack Centre in New Quack City, the spelling has been overlooked and keeps the UK spelling. The same thing happens with the  Anatat Tatanatat cutscene as well. <br />
But the US version also removes the majority of minor cussing the game has to offer, as well as a few other words that weren't really necessary to change. It also adds in an additional line in the ending cutscene. <br /><br />Here is a list of noticeable changes, including a typo mistake.<br />

