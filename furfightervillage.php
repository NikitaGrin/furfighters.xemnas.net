Fur Fighter Village. It may not look much, but it's home to the greatest warriors in animal history. And it's where our walkthrough begins...<br /><br />
<h2>Welcome to the Village!</h2>
After General Bristol has welcomed you, he'll send you straight off to <a href="undermill.php">The Undermill</a> for training. Once you've finished your training (or not, if you'd prefer), you'll be free to roam the village. Let's start this guide by quickly gathering the 50 Gold Tokens hidden around the village, since these will help us open new levels later on. As you exit the Undermill, turn left (ignore the bridge for now, we'll come back to it) and left again until you see the Cape Canardo sign. Grab the 5 Gold Tokens down here, and move left to the side of Tweek's house. You'll see another pathway almost hidden here, so move down it to find Juliette and Chang's homes. Enter Juliette's home from the back (since there's no entry at the sea-front end) and climb the stairs. Look for Juliette's closet, and enter. There's 5 Gold Tokens hidden in this little maze, so grab them (and the <b>Shotgun</b> if you'd like) and  exit. On the balcony there are 5 more Gold Tokens, so grab them and head back out to Gwynth's house (since Chang's house has no Tokens). Enter through the front (where all the gold is), follow the winding ramp up to the top where 5 more Gold Tokens are waiting. Snatch them, and switch to <b>Tweek</b> if you'd like, since it's easier to glide down to Bungalow's home which is just down below. Enter, pass the pillar and climb down the stairs to reach 5 more Gold Tokens. Climb back up the stairs, exit and turn left towards the Undermill, and this time move across the bridge and collect the 5 Gold Tokens on there. Turn left and move past the tree to collect 5 more. There's also a single Token leading down to the subway, so snatch that, turn around and move towards the City of Fear post in the distance. Grab the 5 Gold Tokens in a row, and the 4 just in front of those. Retreat back to the subway post, and you'll see a sign for Beaver Power just behind it. Get over there to grab the final 5 Gold Tokens. 
<br />Now hopefully you collected the <b>Subway Token</b> from the Undermill. If you don't have it, go back and get it! Because we're heading on to <a href="newquackcity.php">New Quack City</a> via the subway. 

<br /><br />
<h2>Second Visit</h2>
Bravo on saving Gwynth! Speaking of which, she can be found resting in her home. Whilst you're here, one of Tweek's siblings has started up a <b>Sliding Block minigame</b> in his room. Completing it in under 60 seconds will earn you the cheat, <b>Rotation Camera</b>. He can be found in the slightly hidden room underneath Gwynth's home. Only Tweek can swim unharmed in the lava, but you could still swim around in time without getting a drastic health drop. You can stop by to play, or you can continue on your journey. So head through to <a href="beaverpower.php">Beaver Power</a>!<br /><br />
<h2>Third Visit</h2>
Great work on saving Juanita. Like Gwynth, she now rests in her home near the sea edge. If you enter her home, one of Rico's children has set up the <b>Snake minigame</b> for you to play. Get a high score of 5000 or more and you will unlock the cheat, <b>Rocket Cam</b>. Like before, you can stop by to play a game (or two) or you can continue on your quest. Onward, to <a href="capecanardo.php">Cape Canardo</a>!

<br /><br />
<h2>Fourth Visit</h2>
Another rescued spouse! Claude can be found resting in Juliette's home. For those who have forgotten, her home lies down a path near the entrance to Cape Canardo, near Gwynth's home. If you decide to visit Juliette's beloved, one of their kittens will initiate a game of <b>Balloon Rescue</b> if you talk to her. Get a high score of --- or more to unlock the <b>Honey I Shrunk The Fur Fighters</b> cheat. Whenever you're ready, head to <a href="dinotopolis.php">Dinotopolis</a>, the entrance to which is near the pond. Not there? Go back to <a href="godmachinevalley.php">God Machine Valley</a> to see what you missed...

<br /><br />
