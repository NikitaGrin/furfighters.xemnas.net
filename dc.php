        <h1>Fur Fighters - Dreamcast</h1>
Fur Fighters started out as a PlayStation/PC game known as 'Furballs', going under the code name of 'Project Drive-By'. In April 1998, the team realised that they were constantly cutting down work so that the PC version worked on the PlayStation as well, as the PlayStation wasn't capable of handling the amibitious project. The decision was made to abandon PlayStation development, and continue high-end PC development, which included a complete re-write.<br />
In August 1998, the Drive-By team were drafted in to create an unrelated 'game-style' concept demo on the Dreamcast for a technical presentation which was to be shown to Sega Japan. At this time, there were still no plans to port the game to the Dreamcast, however.<br />On Thursday 1st October, 1998 at 6.00pm UK time, Bizarre Creations finally announced Furballs officially. It was also announced that it would be released on both PC and Sega Dreamcast.<br />
In March 1999, a silhoutte of the Fur Fighters was revealed. Just over a month later, the first ever screenshots of the Dreamcast version of each Fur Fighter were shown on the official website.  In May 1999, after Bizarre Creations received their Dreamcast Dev Kits, primary development was switched to Dreamcast. After E3 that year, several publishers showed an interest in developing 'Furballs' -  But only one could prevail, and the company that would publish the game  turned out to be Acclaim. Soon after, the name was changed to Fur Fighters, to avoid confusion with a copyrighted American cartoon character (although some sources claim that the name change was to avoid being too 'cutesy'). The game was set to be released in April 2000, but was delayed slightly and instead released in June 2000.<br /><br />
Shortly after it's DC release, it was ported to PC. Two years later, it was re-released with updated graphics, extra levels and bonuses and a general revamp on the Playstation 2 under the title of Fur Fighters: Viggo's Revenge. The game received generally positive reviews, with the only real criticism being the controls, which was hit or miss depending on how one chose to wield them.<br /><br />
The story is based on the <b>Fur Fighters</b>, an elite team of cuddly characters who fight the evil General Viggo. After defeating the evil cat some years before the game begins, the Fur Fighters have retired in a small village with their families, adequetly known as Fur Fighter Village. However, General Viggo has a plot to take over once more....<br /><br />
When three of their babies wake up Roofus the Hound, it's only too clear General Viggo has returned. Reaching for his pistol, Roofus and the other Fur Fighters are knocked out with sleeping gas. General Viggo then kidnaps the babies and scatters them around their worlds (New Quack City, Beaver Power, Dinotopolis, Cape Canardo and Anatat Tatatanat), and transforms their kin into mutant servants. It's up to the Fur Fighters to bring their babies and partners back (in Tweek's case, his brothers, sisters and mother).
<br /><br />
The game was a unique combination of Action, Adventure, Platforming and Shooting. Players have puzzles to solve, baddies to shoot and collectables to find on nearly every level. Bosses exist at the end of each Hub world, which follows General Viggo's plans for world domination.... As well as foiling them.<br /><br />
Players can control any one of the six Fur Fighters as they battle their way through hordes of enemies and mazes. Each Fur Fighter has his/her own special ability that none other can perform, making it vital to use all of them to complete each level.
<br /><br />
The final hub in the game, Viggo a Gogo, is the only hub with no babies to locate in any of the levels. Instead, the focus is on destroying everything precious to your evil nemesis, before finally getting a chance to take him down yourself.

